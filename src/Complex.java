public class Complex {
    int x;
    int y;

    public Complex() {
    }

    public int getY() {
        return y;
    }

    public void setY(int y) {
        this.y = y;
    }

    public int getX() {
        return x;
    }

    public void setX(int x) {
        this.x = x;
    }

    public Complex(int x, int y) {
        this.x = x;
        this.y = y;
    }
}
